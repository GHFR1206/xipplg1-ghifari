    $('.delete').click(function() {

        var productid = $(this).attr('data-id')
        var productnama = $(this).attr('data-nama')

        swal({
                title: "Apakah Kamu Yakin?",
                text: 'Kamu Akan Menghapus Produk "' + productnama + '"',
                icon: "warning",
                buttons: true,
                dangerMode: true,
            })
            .then((willDelete) => {
                if (willDelete) {
                    swal("Produk Berhasil Dihapus", {
                        icon: "success",
                    });
                    window.location = "/delete/" + productid + ""
                } else {
                    swal("Produk Masih Aman");
                    window.location = "/product"
                }
            });
    })

